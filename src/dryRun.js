function createEvent(channelId, content, createdAt = "2026-06-09T00:40:00.000Z") {
  return {
    eventType: "message_create",
    receivedAt: "2026-06-09T00:40:01.000Z",
    createdAt,
    guildId: "guild",
    channelId,
    messageId: `message-${channelId}`,
    messageUrl: `https://discord.com/channels/guild/${channelId}/message-${channelId}`,
    author: {
      id: "author",
      username: "author",
      globalName: "Author",
      tag: "author#0000",
      isBot: false,
    },
    member: {
      displayName: "Author",
    },
    content,
    cleanContent: content,
    parsed: {},
    mentions: [],
    attachments: [],
    embeds: [],
  };
}

